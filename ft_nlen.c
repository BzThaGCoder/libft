#include "libft.h"

int		ft_nlen(uintmax_t n, int base)
{
	int		len;

	if (!n)
		return (1);
	len = 0;
	if (base > 1)
	{
		while (n)
		{
			n /= base;
			len++;
		}
	}
	return (len);
}
